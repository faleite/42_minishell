#include "../../includes/parser.h"

char	*expander_hd(char *s2)
{
	int		i;
	char	*s3;

	i = 0;
	s3 = (char *)malloc(sizeof(char) * (ft_strlen(s2) * 10 + 1));
	while (*s2)
	{
		if (*s2 && *s2 == '$')
		{
			if (*(s2 + 1) == '\"')
				s3[i++] = *(s2)++;
			else if (*(s2 + 1) == '\'')
				(s2)++;
			else if (*(s2 + 1) == '\2' || *(s2 + 1) == '\0')
				s3[i++] = *(s2)++;
			else if (*(s2 + 1) != '\2' && *(s2 + 1) != '\0')
				i = dollar(&s2, &s3, i);
		}
		else if (*s2)
			s3[i++] = *s2++;
	}
	s3[i] = '\0';
	return (s3);
}
