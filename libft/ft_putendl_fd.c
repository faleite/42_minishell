#include "libft.h"

/**
 * @brief Outputs the string â€™sâ€™ to the given file descriptor followed by a
 * newline.
 * @param s The string to output.
 * @param fd The file descriptor on wich to write.
*/
void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
